function math(Zahl1, Zahl2) {
    console.log(Zahl1 * Zahl2); 
    console.log(Zahl1 / Zahl2);
    console.log(`Multiplikation von ${Zahl1} und ${Zahl2}: ${Zahl1 * Zahl2}`);
    console.log(`Division von ${Zahl1} und ${Zahl2}: ${Zahl1 * Zahl2}`);
}

math(10, 2);
math(30, 20);
math(100, 100);
math(5, 0);
math(45, 173);
math(1, 1000);